//pop är en funktion som gör att du kan ta bort det sista elementet i din array. Exempel
let name = ["Björn", "Einar", "Peter"]

name.pop()
//den kommer då loga ut endast Björn och Einar.

//Push är när du vill lägga till ett element.Exempel

let name = ["Peter"]

name.push(Einar)

//Funkar även att ta med flera elemnet samtidigt//
name.push("Einar", "Björn")